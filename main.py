# Module 1: Random Seed Setting
def set_seed(input_str=None):
    """Set the random seed based on user input."""
    if input_str is None:
        # Time-based divination
        seed = int(time.time())
    else:
        try:
            # Number-based divination
            seed = int(input_str) % 2147483647  # Modulo to match Stata's limit
        except ValueError:
            # Question/character-based divination
            seed = int(hashlib.sha256(input_str.encode()).hexdigest(), 16) % 2147483647
    random.seed(seed)

# Module 2: Hexagram Generation
def generate_hexagram():
    """Generate a hexagram by simulating three-coin tosses for six lines."""
    hexagram = []
    for _ in range(6):
        coin1 = random.randint(0, 1)
        coin2 = random.randint(0, 1)
        coin3 = random.randint(0, 1)
        coinsum = coin1 + coin2 + coin3
        if coinsum == 3:
            hexagram.append((1, 0))  # Yang changing to Yin
        elif coinsum == 2:
            hexagram.append((0, 0))  # Yin unchanging
        elif coinsum == 1:
            hexagram.append((1, 1))  # Yang unchanging
        elif coinsum == 0:
            hexagram.append((0, 1))  # Yin changing to Yang
    return hexagram

# Module 3: Divination Logic
def divine(hexagram):
    """Determine the divination result based on the hexagram and changing lines."""
    original = ''.join([str(line[0]) for line in hexagram])
    changed = ''.join([str(line[1]) for line in hexagram])
    changes = sum(1 for line in hexagram if line[0] != line[1])

    if changes == 0:
        return HEXAGRAM_DICT[original]['gua']
    elif changes == 1:
        for i, line in enumerate(hexagram):
            if line[0] != line[1]:
                return HEXAGRAM_DICT[original]['yao'][i + 1]
    elif changes == 2:
        result = []
        for i, line in enumerate(hexagram):
            if line[0] != line[1]:
                result.append(HEXAGRAM_DICT[original]['yao'][i + 1])
        return '\n'.join(result)
    elif changes == 3:
        if hexagram[5][0] == hexagram[5][1]:  # Top line unchanged
            return f"{HEXAGRAM_DICT[original]['gua']}\n{HEXAGRAM_DICT[changed]['gua']}\n请进一步看对应彖辞"
        else:
            return f"{HEXAGRAM_DICT[changed]['gua']}\n{HEXAGRAM_DICT[original]['gua']}\n请进一步看对应彖辞"
    elif changes == 4:
        result = []
        for i, line in enumerate(hexagram):
            if line[0] == line[1]:
                result.append(HEXAGRAM_DICT[changed]['yao'][i + 1])
        return '\n'.join(result)
    elif changes == 5:
        for i, line in enumerate(hexagram):
            if line[0] == line[1]:
                return HEXAGRAM_DICT[changed]['yao'][i + 1]
    elif changes == 6:
        if original == '111111':
            return '用九：见群龙无首，吉。《象》曰：用九，天德不可为首也。'
        elif original == '000000':
            return '用六：利，永贞。《象》曰：用六永贞，以大终也。'
        else:
            return HEXAGRAM_DICT[changed]['gua']

# Module 4: Main Function and Output
def main(input_str=None):
    """Coordinate the divination process and return the result as a string."""
    set_seed(input_str)
    hexagram = generate_hexagram()
    original = ''.join([str(line[0]) for line in hexagram])
    changed = ''.join([str(line[1]) for line in hexagram])

    # Build result string
    result = []
    current_time = time.strftime("%Y-%m-%d %H:%M:%S", time.localtime())
    result.append(f"日期：今天是 {current_time}")
    result.append("假尔泰筮有常，\n今以事，未知可否。\n爰质所疑于神之灵，\n吉凶得失、悔吝忧虞，\n唯尔有神，尚明告之。\n")
    result.append("起卦————\n")

    # Hexagram
    result.append("卦象：")
    for i, line in enumerate(hexagram[::-1]):  # Reverse to match traditional top-down order
        original_yao = "阳" if line[0] == 1 else "阴"
        changed_yao = "阳" if line[1] == 1 else "阴"
        result.append(f"{6 - i}爻: {original_yao} -> {changed_yao}")
    result.append(f"为 {HEXAGRAM_DICT[original]['name']} 之 {HEXAGRAM_DICT[changed]['name']} 卦。\n")

    # Divination result
    result.append("这是《易》中的卦/爻辞与注释，两条辞则以前者为主：")
    result.append(divine(hexagram))

    # Join all lines into a single string
    return '\n'.join(result)

